package org.calminfotech.admin.forms;

public class SubServiceItemForm {
	
	private Integer id;

	private Integer itemName;
	
	private Integer subServiceId;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getItemName() {
		return itemName;
	}

	public void setItemName(Integer itemName) {
		this.itemName = itemName;
	}

	public Integer getSubServiceId() {
		return subServiceId;
	}

	public void setSubServiceId(Integer subServiceId) {
		this.subServiceId = subServiceId;
	}
}
