package org.calminfotech.system.forms;


public class CategoryItemUnitForm {
	

	private Integer itemUnitId;
	
	private Integer unit;	
	
	private Integer categoryItem;

	//Getters and Setters
	
	public Integer getItemUnitId() {
		return itemUnitId;
	}

	public void setItemUnitId(Integer itemUnitId) {
		this.itemUnitId = itemUnitId;
	}

	public Integer getUnit() {
		return unit;
	}

	public void setUnit(Integer unit) {
		this.unit = unit;
	}

	public Integer getCategoryItem() {
		return categoryItem;
	}

	public void setCategoryItem(Integer categoryItem) {
		this.categoryItem = categoryItem;
	}

	

}
