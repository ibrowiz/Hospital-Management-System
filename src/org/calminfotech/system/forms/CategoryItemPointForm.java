package org.calminfotech.system.forms;


public class CategoryItemPointForm {
	

	private Integer itemPointId;
	
	private Integer flowPointId;	
	
	private Integer categoryItem;

	//Getters and Setters
	

	public Integer getItemPointId() {
		return itemPointId;
	}

	public void setItemPointId(Integer itemPointId) {
		this.itemPointId = itemPointId;
	}

	public Integer getFlowPointId() {
		return flowPointId;
	}

	public void setFlowPointId(Integer flowPointId) {
		this.flowPointId = flowPointId;
	}

	public Integer getCategoryItem() {
		return categoryItem;
	}

	public void setCategoryItem(Integer categoryItem) {
		this.categoryItem = categoryItem;
	}

	

}
