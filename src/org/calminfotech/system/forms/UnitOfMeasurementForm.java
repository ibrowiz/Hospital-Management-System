package org.calminfotech.system.forms;



public class UnitOfMeasurementForm {	

	private Integer unitId;
	
	private String unitOfMeasure;
	
	private String unitAbbreviate;

	//Getters and Setters
	
	public Integer getUnitId() {
		return unitId;
	}

	public void setUnitId(Integer unitId) {
		this.unitId = unitId;
	}

	public String getUnitOfMeasure() {
		return unitOfMeasure;
	}

	public void setUnitOfMeasure(String unitOfMeasure) {
		this.unitOfMeasure = unitOfMeasure;
	}

	public String getUnitAbbreviate() {
		return unitAbbreviate;
	}

	public void setUnitAbbreviate(String unitAbbreviate) {
		this.unitAbbreviate = unitAbbreviate;
	}
	
	
	
	
	
	
	
	

}
