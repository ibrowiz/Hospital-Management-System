package org.calminfotech.consultation.forms;





public class VisitLaboratoryTestForm {
	
	
	private Integer id;

	private Integer visitId;
	

	private Integer patientId;


	private Integer labtestId;
	

	private String other_detail;


	public Integer getId() {
		return id;
	}


	public void setId(Integer id) {
		this.id = id;
	}


	public Integer getVisitId() {
		return visitId;
	}


	public void setVisitId(Integer visitId) {
		this.visitId = visitId;
	}


	public Integer getPatientId() {
		return patientId;
	}


	public void setPatientId(Integer patientId) {
		this.patientId = patientId;
	}


	public Integer getLabtestId() {
		return labtestId;
	}


	public void setLabtestId(Integer labtestId) {
		this.labtestId = labtestId;
	}


	public String getOther_detail() {
		return other_detail;
	}


	public void setOther_detail(String other_detail) {
		this.other_detail = other_detail;
	}



}
