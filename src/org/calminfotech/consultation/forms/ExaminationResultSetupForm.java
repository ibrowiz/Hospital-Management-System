package org.calminfotech.consultation.forms;




public class ExaminationResultSetupForm {
	
private Integer globalExamResultSetupId;
	
	private Integer examId;

	private String examResultName ;

	public Integer getGlobalExamResultSetupId() {
		return globalExamResultSetupId;
	}

	public void setGlobalExamResultSetupId(Integer globalExamResultSetupId) {
		this.globalExamResultSetupId = globalExamResultSetupId;
	}

	public Integer getExamId() {
		return examId;
	}

	public void setExamId(Integer examId) {
		this.examId = examId;
	}

	public String getExamResultName() {
		return examResultName;
	}

	public void setExamResultName(String examResultName) {
		this.examResultName = examResultName;
	}
	
	
	
}
